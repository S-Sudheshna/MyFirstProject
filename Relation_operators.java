public class Relation_operators {
    public static void main(String[] args) {
        int a=10,b=30,c=30;
        System.out.println("a= "+a+"\t"+ "b= "+b+"\t"+"c= "+c);
        System.out.println("a>b=" +(a>b)+"\t"+"a<b=" +(a<b));
        System.out.println("b>c="+(b>c)+"\t"+"b<c="+(b<c));
        System.out.println("a>=b:"+(a>=b)+"\t"+"a<=b:"+(a<=b));
        System.out.println("b>=c:"+(b>=c)+"\t"+"b<=c:"+(b<=c));
    }
}
