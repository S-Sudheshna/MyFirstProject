public class Logical_operators {
    public static void main(String[] args) {
        boolean a=true,b=false;
        System.out.println("a=" + a+"\t"+ "b= " +b);
        System.out.println("a && b : "+(a&&b));
        System.out.println("a || b :" +(a||b));
        System.out.println("! a : " +!(a));
    }
}
