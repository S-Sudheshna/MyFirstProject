public class Variablesdemo {
    public static void main(String[] args) {
        boolean a=true;
        byte b=10;
        short s=2;
        int i=35;
        long l=10000;
        float f=45.89f;
        double d=250.69871d;
        char c='m';
        System.out.println("this is a boolean variable a="+a);
        System.out.println("this is a byte variable b="+b);
        System.out.println("this is a short variable s="+s);
        System.out.println("this is an integer variable i="+i);
        System.out.println("this is  a long variable l="+l);
        System.out.println("this is a float variable f="+f);
        System.out.println("this is a double variable d="+d);
        System.out.println("this is a character variable c="+c);
        

    }
    
}
